package Restudy_java;
//クラスを指定
public class Kiso_1A1 {
	
	public static void main(String []args) {
		int box1 = 12;
		int box2 = 55;
		
		System.out.println("和＝"+ (box1 + box2));
		System.out.println("差＝"+ (box1 - box2));
		System.out.println("積＝"+ (box1 * box2));
		System.out.println("商＝"+ (box1 / box2));
		System.out.println("余り＝"+ (box1 % box2));
	}
}
