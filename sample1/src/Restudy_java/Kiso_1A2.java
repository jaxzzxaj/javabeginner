package Restudy_java;
//クラスを指定
public class Kiso_1A2 {
	
	public static void main(String[] args) {
//		整数を文字列で宣言
		String str1 = "234";
		String str2 = "123";
//		文字列を整数にキャスト
		int i1 = Integer.parseInt(str1);
		int i2 = Integer.parseInt(str2);
//		出力
		System.out.println(i1 + i2);
		System.out.println(i1 - i2);
		System.out.println(i1 * i2);
		System.out.println(i1 / i2);
		System.out.println(i1 % i2);
	}
	
	
}
